-- 3) WHERE Clause
SELECT * FROM CLIENT_MASTER WHERE STATE = 'GUJARAT';
SELECT * FROM PRODUCT_MASTER WHERE SELLPRICE > 1000;

-- 4) ORDER BY (Ascending and Descending)
SELECT * FROM CLIENT_MASTER ORDER BY NAME ASC;
SELECT * FROM PRODUCT_MASTER ORDER BY SELLPRICE DESC;

-- 5) Create Table from Existing Table (with data)
CREATE TABLE CLIENT_GUJARAT AS
SELECT * FROM CLIENT_MASTER WHERE STATE = 'GUJARAT';

-- 6) Create Table from Existing Table (without data)
CREATE TABLE PRODUCT_EMPTY AS
SELECT * FROM PRODUCT_MASTER WHERE 1=0;

-- 7) DELETE Statement
DELETE FROM SALESMAN_MASTER WHERE CITY = 'MUMBAI';

-- 8) UPDATE Statement
UPDATE CLIENT_MASTER SET BALDUE = BALDUE + 100 WHERE CLIENTNO = '1Y093';
UPDATE PRODUCT_MASTER SET SELLPRICE = 900.00 WHERE DESCRIPTION = 'TV';

