------------------------------------------------------------
-- Step 1: Create Tables
------------------------------------------------------------

-- CLIENT_MASTER Table
CREATE TABLE CLIENT_MASTER (
    CLIENTNO VARCHAR(6) UNIQUE NOT NULL,
    NAME VARCHAR(7) UNIQUE NOT NULL,
    ADDRESS1 VARCHAR(30),
    ADDRESS2 VARCHAR(30),
    CITY VARCHAR(15),
    PINCODE NUMERIC(6),
    STATE VARCHAR(15),
    BALDUE NUMERIC(10,2)
);

-- PRODUCT_MASTER Table
CREATE TABLE PRODUCT_MASTER (
    PRODUCTNO VARCHAR(6),
    DESCRIPTION VARCHAR(15),
    PROFITPERCENT NUMERIC(4,2),
    UNITMEASURE VARCHAR(10),
    QTYONHAND NUMERIC(8),
    REORDERLVL NUMERIC(8),
    SELLPRICE NUMERIC(8,2),
    COSTPRICE NUMERIC(8,2)
);

-- SALESMAN_MASTER Table
CREATE TABLE SALESMAN_MASTER (
    SALESMANNO VARCHAR(6),
    SALESMANNAME VARCHAR(20),
    ADDRESS1 VARCHAR(30),
    ADDRESS2 VARCHAR(30),
    CITY VARCHAR(20),
    PINCODE NUMERIC(6),
    STATE VARCHAR(20),
    SALAMT NUMERIC(8,2),
    TGTTOGET NUMERIC(6,2),
    YTDSALES NUMERIC(6,2),
    REMARKS VARCHAR(60)
);

